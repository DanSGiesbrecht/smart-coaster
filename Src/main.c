/* StartDefaultTask function */
void StartDefaultTask(void const * argument)
{

  /* USER CODE BEGIN 5 */
  /* Infinite loop */
  for(;;)
  {
      if( UART_CheckSending() )
      {
          vTaskDelay( pdMS_TO_TICKS( 100 ) );
      }
      else
      {
          char* toSend = "hello world\r\n";
          UART_Send( ( uint8_t* )toSend, strlen( toSend ) );
      }
  }
  /* USER CODE END 5 */ 
}

